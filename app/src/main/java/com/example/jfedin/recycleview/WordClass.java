package com.example.jfedin.recycleview;

public class WordClass {

    private int mImageView;
    private String mTextV1;
    private String mTextV2;

    public WordClass(int mImageView, String mTextV1, String mTextV2) {
        this.mImageView = mImageView;
        this.mTextV1 = mTextV1;
        this.mTextV2 = mTextV2;
    }

    public int getmImageView() {
        return mImageView;
    }

    public String getmTextV1() {
        return mTextV1;
    }

    public String getmTextV2() {
        return mTextV2;
    }
}


